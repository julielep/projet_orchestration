"""API/maths.py
Oprérations mathématiques simple.
"""

def add(a:float, b:float) -> float:
    """Ajoute deux nombres.

    Args:
        a (float): Nombre 
        b (float): Nombre

    Returns:
        float: a + b
    """
    return a+b

def multiply(a:float, b:float) -> float:
    """Multiplie deux nombres.

    Args:
        a (float): Nombre
        b (float): Nombre

    Returns:
        float: a * b
    """
    return a * b